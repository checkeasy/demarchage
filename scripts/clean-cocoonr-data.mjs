import { createClient } from "@supabase/supabase-js";
const sb = createClient(process.env.NEXT_PUBLIC_SUPABASE_URL, process.env.SUPABASE_SERVICE_ROLE_KEY);
const WS = '83da732a-a933-4ed4-a815-3f975c8ff0c6';

const { data: concs } = await sb.from('prospects')
  .select('id, company, email, phone, website, location, custom_fields')
  .eq('workspace_id', WS)
  .eq('job_title', 'Conciergerie')
  .order('company')
  .range(0, 999);

console.log(`Total conciergeries: ${concs?.length}\n`);

// 1. Remove Cocoonr corporate phone number from partners
const COCOONR_PHONE = '0928389844';
let phoneCleaned = 0;
const cocoonrOffices = [];

for (const c of concs || []) {
  if (c.phone === COCOONR_PHONE) {
    if (c.company.startsWith('Cocoonr ')) {
      // This is a Cocoonr office, not a partner - mark it
      cocoonrOffices.push(c);
    } else {
      // Partner with Cocoonr's phone - remove the phone
      await sb.from('prospects').update({ phone: null, updated_at: new Date().toISOString() }).eq('id', c.id);
      phoneCleaned++;
    }
  }
}
console.log(`Phone Cocoonr nettoyees (partenaires): ${phoneCleaned}`);
console.log(`Bureaux Cocoonr identifies: ${cocoonrOffices.length}`);
for (const o of cocoonrOffices) {
  console.log(`  - ${o.company}`);
}

// 2. Mark fake emails (domain doesn't resolve = site was OFFLINE)
let fakeEmails = 0;
for (const c of concs || []) {
  if (!c.email) continue;
  const domain = c.email.split('@')[1];
  if (!domain) continue;

  // If the enricher visited this domain and it was offline, the email is probably fake
  if (c.custom_fields?.direct_enriched_at && !c.custom_fields?.site_title && !c.custom_fields?.description) {
    // Domain was tried and nothing found = email is likely auto-generated
    const isAutoGenerated = c.email.startsWith('contact@') && domain.endsWith('.fr');
    if (isAutoGenerated) {
      // Store as potential_email in custom_fields, set email to a more honest value
      const cf = { ...(c.custom_fields || {}), potential_email: c.email, email_status: 'unverified_domain' };
      await sb.from('prospects').update({
        custom_fields: cf,
        updated_at: new Date().toISOString(),
      }).eq('id', c.id);
      fakeEmails++;
    }
  }
}
console.log(`Emails marques comme non-verifies: ${fakeEmails}`);

// 3. Final stats
const { data: updated } = await sb.from('prospects')
  .select('id, company, email, phone, website, custom_fields')
  .eq('workspace_id', WS)
  .eq('job_title', 'Conciergerie')
  .order('company')
  .range(0, 999);

let realPhone = 0, realWeb = 0, realEmail = 0, unverified = 0;
for (const c of updated || []) {
  if (c.phone) realPhone++;
  if (c.website && !c.website.includes('cocoonr')) realWeb++;
  if (c.custom_fields?.email_status === 'unverified_domain') unverified++;
  else if (c.email && !c.email.includes('exemple')) realEmail++;
}

console.log(`\n=== ETAT FINAL ===`);
console.log(`Conciergeries totales: ${updated?.length}`);
console.log(`  Avec telephone: ${realPhone}`);
console.log(`  Avec site web reel: ${realWeb}`);
console.log(`  Avec email verifie: ${realEmail}`);
console.log(`  Email non-verifie (domaine offline): ${unverified}`);
